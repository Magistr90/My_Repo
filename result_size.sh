#!/bin/bash

# Функция для анализа и отображения размеров файлов и директорий
analyze_sizes() {
    # Используем команду 'du' для получения размер файлов и директори
    for item in *; do
        # Проверяем, существует ли элемент (на случай, если папка пуста)
        if [ -e "$item" ]; then
            size=$(du -sh "$item" | cut -f1)  # 'cut' используется для получения только размера
            echo "$size  $item"  # Выводим размер и имя файла/директории
        fi
    done
}

# Вызов функции и сортировка по убыванию
analyze_sizes | sort -h -r
